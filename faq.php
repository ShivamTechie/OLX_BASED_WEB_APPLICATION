                    How Auctions Work?
                  </a>
                </h4>
              </div>
              <div id="collapseTwo" class="panel-collapse collapse">
                <div class="panel-body">
                  <p>
                    Listing an Auction: Sellers create an auction listing by providing detailed information about the item they wish to sell. This includes a description, photos, starting bid, reserve price (if any), and auction duration.
                    <br>
                    Setting Up Bidding Parameters: Sellers set the starting bid amount and, optionally, a reserve price—the minimum amount they are willing to accept for the item. The auction duration determines how long the auction will be active.
                    <br>
                    Bidding Process: Buyers place bids on the auction item. Each bid must be higher than the previous one, and the current highest bid is visible to all participants. Bidders can place as many bids as they want within the auction period.
                    <br>

                    Auction Monitoring: Sellers and bidders can monitor the auction’s progress in real-time. Alerts or notifications may be provided for significant actions, such as receiving a new bid or nearing the auction's end.
                    <br>
                    Auction End: When the auction reaches its end time, the highest bid wins, provided it meets or exceeds the reserve price (if one was set). If the reserve price is not met, the item may not be sold.
                    <br>
                    Winning the Auction: The winning bidder receives a notification that they have won the auction. They are then required to complete the payment process as per the auction's terms.
                    <br>
                    Payment and Fulfillment: The seller and winning bidder arrange payment and delivery of the item. Payment methods and shipping arrangements are typically agreed upon after the auction ends.
                    <br>
                    Post-Auction: After the auction concludes, the seller ships the item to the winning bidder. Both parties may have the opportunity to leave feedback or a rating about the transaction, depending on the platform's features.
                </div>
